'''
python program for simple calculator using class function
'''

class calculator:
    """
    This class holds operations to perform arithmetic calculations

    instance method:

    """

# function to addition two numbers
    def add(num1,num2):
        return num1+num2

# function to subtract two numbers
    def subtract(num1,num2):
        return num1-num2

# function to multiply two numbers
    def multiply(num1,num2):
        return num1*num2

# function to divide two numbers
    def divide(num1,num2):
        return num1/num2

